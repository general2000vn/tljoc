<?php
declare(strict_types=1);

namespace App\Controller;

use App\Model\Table\RolesTable;

/**
 * Roles Controller
 *
 * @property \App\Model\Table\RolesTable $Roles
 * @method \App\Model\Entity\Role[]|\Cake\Datasource\ResultSetInterface paginate($object = null, array $settings = [])
 */
class RolesController extends AppController
{
    
    public function initialize(): void
    {
        parent::initialize();

        $this->set('menuElement', 'sash/left-menu-admin');
    }
    
    /**
     * Index method
     *
     * @return \Cake\Http\Response|null|void Renders view
     */
    public function index()
    {
        $roles = $this->paginate($this->Roles);

        $this->set(compact('roles'));
    }

    /**
     * View method
     *
     * @param string|null $id Role id.
     * @return \Cake\Http\Response|null|void Renders view
     * @throws \Cake\Datasource\Exception\RecordNotFoundException When record not found.
     */
    public function view($id = null)
    {
        $role = $this->Roles->get($id, [
            'contain' => ['Users.Departments'],
            
        ]);

        $this->set(compact('role'));
    }

    /**
     * Add method
     *
     * @return \Cake\Http\Response|null|void Redirects on successful add, renders view otherwise.
     */
    public function add()
    {

        if (!$this->isAllowed([RolesTable::R_SADMIN])){
            $this->Flash->error('You dont have priviledge for this action!');
            $this->redirect($this->referer());
        }
        
        $role = $this->Roles->newEmptyEntity();
        if ($this->request->is('post')) {
            $role = $this->Roles->patchEntity($role, $this->request->getData());
            if ($this->Roles->save($role)) {
                $this->Flash->success(__('The role has been saved.'));

                return $this->redirect(['action' => 'index']);
            }
            $this->Flash->error(__('The role could not be saved. Please, try again.'));
        }
        $users = $this->Roles->Users->find('list', ['conditions' => ['is_deleted' => false],
                                                //'fields' => ['id', 'firstname', 'lastname',],
                                                
                                                'order' => ['firstname' => 'ASC', 'lastname' => 'ASC']
                                            ]);
        $this->set(compact('role', 'users'));
    }

    /**
     * Edit method
